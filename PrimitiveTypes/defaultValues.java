package PrimitiveTypes;

public class defaultValues {

	public static void main(String[] args) {
		byte a=0;
		System.out.println("Default value of byte:  "+a);
		short b=0;
		System.out.println("Default value of short:  "+b);
		int c=0;
		System.out.println("Default value of integer:  "+c);
		long d=0L;
		System.out.println("Default value of long:  "+d);
		float e=0.0f;
		System.out.println("Default value of float:  "+e);
		double f=0.0d;
		System.out.println("Default value of double:  "+f);
		boolean g=false;
		System.out.println("Default value of boolean:  "+g);
		char h='\u0000';
		System.out.println("Default value of character:  "+h);

	}

}
